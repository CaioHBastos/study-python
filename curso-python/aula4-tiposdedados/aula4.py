"""
Tipos de dados
str - string -> textos 'Assim' "Assim"
int - inteiro -> 123456, 0, -1
float - real/ponto flutuante -> 10.50, 1.5, -10.40
bool - boolean/lógico -> True/False
"""
print('Luiz', type('Luiz'))
print(123456, type(123456))
print(1.10, type(1.10))
print(True, type(True))
print(10 == 10, type(10 == 10))

# TypeCasting
print('Caio', type('Caio'), bool('Caio'))
print('10', type('10'), type(int('10')))

