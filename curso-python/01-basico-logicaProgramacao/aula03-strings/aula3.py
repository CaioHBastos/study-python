"""
str - string -> Texto que estão entre aspas
"""
print('Alguma coisa str - string')
print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')

# Maneiras ruins:
print("Esse é meu \"texto\" (str).")
print('Esse é meu \'texto\' (str).')

# Com barra N
print('Esse é meu \n (str).')
print(r'Esse é meu \n (str).')
